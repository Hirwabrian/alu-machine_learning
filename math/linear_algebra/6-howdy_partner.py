#!/usr/bin/env python3

"""
Module for concatenating two 1D arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two 1D arrays.

    Args:
        arr1 (list): First array.
        arr2 (list): Second array.

    Returns:
        list: New array containing elements of both inputs.
    """
    return arr1 + arr2
